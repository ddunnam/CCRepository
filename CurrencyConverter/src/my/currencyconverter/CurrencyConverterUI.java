    private void convertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertButtonActionPerformed
        //send the comboBox values to the Convert bject
        convert.update(currentComboBox.getSelectedItem().toString());
        rateText.setText(convert.getRate(currentComboBox.getSelectedItem().toString(), desiredComboBox.getSelectedItem().toString()));
        desiredText.setText(convert.getConvertValue(currentComboBox.getSelectedItem().toString(), desiredComboBox.getSelectedItem().toString(), currentValueText.getText()));
        
        Double currentDouble = Double.valueOf(currentValueText.getText());
        DecimalFormat decim = new DecimalFormat("0.00");
        Double dbl = Double.parseDouble(decim.format(currentDouble));
        currentValueText.setText(String.valueOf(dbl));
    }//GEN-LAST:event_convertButtonActionPerformed

